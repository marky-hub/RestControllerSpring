package demo.model;

public class Student {
private String id;
private String name;
private Double grade;
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public Double getGrade() {
	return grade;
}
public void setGrade(Double grade) {
	this.grade = grade;
}


}
